                a Delícias caseiras em 2004 Sempre preocupados em servir bem, traziam sempre novidades e pedidos dos clientes. Pouco a pouco a Delícias caseiras foi crescendo, e o espaço inicial, tornou-se pequeno para tanta variedade. Precisaram de um novo espaço, muito maior que o primeiro, mudaram-se então para o prédio proprio com 600m2, montando uma loja moderna, com auto-serviço e assim, criando sua própria identidade: uma loja de Doces.
                Grande e informatizada, mas preservando o espírito familiar, a loja foi crescendo, e assim continua até hoje em sua expansão, seja no espaço físico da loja, hoje com 1200m2, seja no espaço cibernético com a inauguração da loja virtual.
                Então, se precisar de um docinho pra comer em casa, de produtos para sua padaria, lanchonete, cantina, cafeteria, de materiais para chocolate caseiro, cestas de café da manhã, sorveteria e confeitaria, de embalagens diversas, de artigos para a festa de seus filhos, com preço justo e confiança, visite uma de nossas unidades.
